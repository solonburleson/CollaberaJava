package PersonArrayProcessing;

public class Person {
	private String name;
	private int age;
	private char gender;
	
	public String getName() {
		return name;
	}
	
	public void setName(String n) {
		name = n;
	}
	
	public int getAge() {
		return age;
	}
	
	public void setAge(int a) {
		age = a;
	}
	
	public char getGender() {
		return gender;
	}
	
	public void setGender(char g) {
		gender = g;
	}
}
